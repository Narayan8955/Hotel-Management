    private void txtcustomeridMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtcustomeridMouseClicked
        // TODO add your handling code here:
        txtcustomerid.setText("");
        txtcustomerid.setForeground(Color.BLACK);
         try
       {
           Class.forName("com.mysql.jdbc.Driver");
       Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/tajhotel","root","root");
       Statement stmt=con.createStatement();
       
       String qry="select count(*)from customerentry";
       ResultSet rs=stmt.executeQuery(qry);
       if(rs.next())
       {
        int count=rs.getInt(1);
        count=count+1;
        txtcustomerid.setText(count+"");
       
       }
       }
       catch(Exception ex)
       {
       JOptionPane.showMessageDialog(this, ex);
       }
        
        
        
    }//GEN-LAST:event_txtcustomeridMouseClicked

