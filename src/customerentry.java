    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       try
       {
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/tajhotel","root","root");
            Statement stmt=con.createStatement();
           
            int customerid=Integer.parseInt(txtcustomerid.getText());
             String customername=txtcustomername.getText();
            String mobileno=txtmobileno.getText();
            String emailid=txtemailid.getText();
          
            
            String dateStr=txtdate.getDate().toString();
            SimpleDateFormat formatter = new SimpleDateFormat("E MMM dd HH:mm:ss Z yyyy");
            Date date = (Date)formatter.parse(dateStr);  

            Calendar cal = Calendar.getInstance();
            cal.setTime(date);
            String entrydate =  cal.get(Calendar.YEAR) +  "/" + (cal.get(Calendar.MONTH) + 1) + "/" + cal.get(Calendar.DATE);
            String address=txtaddress.getText();
            
            String qry="insert into customerentry values('"+customerid+"','"+customername+"','"+mobileno+"','"+emailid+"','"+entrydate+"','"+address+"')";
            stmt.executeUpdate(qry);
            
            
            
             JOptionPane.showMessageDialog(this,"Entry Successfully");
           
        new roomdetails().show();
        this.dispose();
        }
        catch (Exception ex)
        {
        JOptionPane.showMessageDialog(this, ex);   
        }
        
       
    }//GEN-LAST:event_jButton1ActionPerformed

